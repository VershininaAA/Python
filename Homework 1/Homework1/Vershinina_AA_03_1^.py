# 3.Вычислить и вывести на экран период колебания маятника длиной L
# с точностью до сотых.  Для рассчетов использовать формулу T = 2π√(L/g),
# где g – ускорение свободного падения (9.81 м/c2).
# Значение длины маятника в метрах необходимо ввести с клавиатуры.

