# 1.Вычислить и вывести на экран длину окружности и
#  площадь круга одного и того же заданного радиуса R,
#  который необходимо ввести с клавиатуры в сантиметрах.
#   Результаты должны округляться до сотых.
import math
r= float(input("Введите радиус окружности: "))
l= round(r*math.pi*2,2)
s= round(math.pi*r**2,2)
print("l=", l)
print(" ,S=", s)

